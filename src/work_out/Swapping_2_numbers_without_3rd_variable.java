package work_out;

public class Swapping_2_numbers_without_3rd_variable {

	public static void main(String[] args) {
		int a=30;
		int b=40;
		System.out.println("before swapping a="+a+","+"b="+b);
		a=a+b;//70
		b=a-b;//30
		a=a-b;//40
		System.out.println("after swapping a="+a+","+"b="+b);

	}

}
